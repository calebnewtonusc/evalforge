class BenchmarkCorpusIndexer:
    """
    Index major benchmark corpora.

    For each benchmark, produces:
    - items.jsonl: representative items with metadata
    - issues.json: known contamination/shortcut issues
    - critique_summary.json: critique paper summaries
    """

    def __init__(self, output_dir: str | Path) -> None:
        self.output_dir = Path(output_dir)
        self.output_dir.mkdir(parents=True, exist_ok=True)

    def run(self, benchmarks: list[str] | None = None) -> dict[str, int]:
        """
        Index specified benchmarks.

        Returns:
            Dict mapping benchmark name → number of items indexed.
        """
        if benchmarks is None:
            benchmarks = list(BENCHMARK_SOURCES.keys())

        results: dict[str, int] = {}
        for name in benchmarks:
            if name not in BENCHMARK_SOURCES:
                logger.warning(f"Unknown benchmark: {name}")
                continue
            logger.info(f"Indexing {name}...")
            try:
                n = self._index_benchmark(name, BENCHMARK_SOURCES[name])
                results[name] = n
                logger.info(f"  {name}: {n} items indexed")
            except Exception as e:
                logger.error(f"  {name}: failed — {e}")
                results[name] = 0

        return results

    def _index_benchmark(self, name: str, config: dict[str, Any]) -> int:
        """Index one benchmark. Returns number of items saved."""
        bench_dir = self.output_dir / name
        bench_dir.mkdir(exist_ok=True)

        # Save known issues and critique papers
        meta = {
            "name": name,
            "description": config.get("description", ""),
            "known_issues": config.get("known_issues", []),
            "critique_papers": config.get("critique_papers", []),
        }
        (bench_dir / "meta.json").write_text(json.dumps(meta, indent=2))

        # Load items via HuggingFace datasets
        hf_dataset = config.get("hf_dataset")
        if hf_dataset is None:
            logger.debug(f"  {name}: no HuggingFace dataset, skipping item download")
            return 0

        try:
            from datasets import load_dataset  # nosec B615

            items_saved = 0
            all_items: list[dict] = []

            for subset in config.get("subsets", ["default"]):
                try:
                    ds = load_dataset(  # nosec B615
                        hf_dataset, subset, split="test", trust_remote_code=True
                    )
                    for i, item in enumerate(ds):
                        if i >= 500:  # sample 500 items per subset for metadata
                            break
                        enriched = {
                            "benchmark": name,
                            "subset": subset,
                            "index": i,
                            "item": dict(item),
                            "known_issues": config.get("known_issues", []),
                        }
                        all_items.append(enriched)
                        items_saved += 1
                except Exception as e:
                    logger.debug(f"  Could not load {hf_dataset}/{subset}: {e}")

            if all_items:
                items_path = bench_dir / "items.jsonl"
                items_path.write_text(
                    "\n".join(json.dumps(item) for item in all_items) + "\n"
                )

            return items_saved

        except ImportError:
            logger.warning(
                "datasets package not available — install with: pip install datasets"
            )
            return 0


@retry(stop=stop_after_attempt(3), wait=wait_exponential(min=1, max=10))
def _fetch_url(url: str) -> str:
    resp = requests.get(url, timeout=30)
    resp.raise_for_status()
    return resp.text


